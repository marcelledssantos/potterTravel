package passagem_aerea;

public class Passagem_aerea {
 
	private int id; 
	private int id_viagem;
	private int id_cia_aerea;
	
	public Passagem_aerea() {}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId_viagem() {
		return id_viagem;
	}
	public void setId_viagem(int id_viagem) {
		this.id_viagem = id_viagem;
	}
	
	public int getId_cia_aerea() {
		return id_cia_aerea;
	}
	public void setId_cia_aerea(int id_cia_aerea) {
		this.id_cia_aerea = id_cia_aerea;
	}
}


